local ui    = require("openmw.ui")
local util  = require("openmw.util")
local const = require('scripts.ErnMMUI.render.const')




local function lerpColor(a, b, t)
    return util.color.rgba(
        a.r + (b.r - a.r) * t,
        a.g + (b.g - a.g) * t,
        a.b + (b.b - a.b) * t,
        a.a + (b.a - a.a) * t
    )
end

return {
    lerpColor = lerpColor
}
